def PlotTriangle(f0,ax,c):
    ax.plot([f0[0][0],f0[1][0]],[f0[0][1],f0[1][1]],[f0[0][2],f0[1][2]],c);
    ax.plot([f0[1][0],f0[2][0]],[f0[1][1],f0[2][1]],[f0[1][2],f0[2][2]],c);
    ax.plot([f0[2][0],f0[0][0]],[f0[2][1],f0[0][1]],[f0[2][2],f0[0][2]],c);
    
